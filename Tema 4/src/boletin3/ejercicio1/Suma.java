package boletin3.ejercicio1;

public class Suma {

	/**
	 * Suma de dos enteros
	 * @param num1 El primer entero
	 * @param num2 El segundo entero
	 * @return La suma de los 2 enteros
	 */
	static int suma(int num1, int num2) {
	return num1+num2;	
	}
	
	/**
	 * Suma de dos doubles
	 * @param num1 El primer double
	 * @param num2 El segundo double
	 * @return La suma de los doubles
	 */
	static double suma(double num1, double num2) {
		return num1+num2;
	}
	
}
