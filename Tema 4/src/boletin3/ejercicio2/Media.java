package boletin3.ejercicio2;

public class Media {

	/**
	 * Media de dos enteros

	 */
	static double media(int n1, int n2) {
		return (n1 + n2) / 2;
	}

	/**
	 * Media de tres enteros
	 * @param n1 Primer entero
	 * @param n2 Segundo entero
	 * @param n3 Tercer entero
	 * @return La media de los enteros, tipo double
	 */
	static double media(int n1, int n2, int n3) {
		return (n1 + n2 + n3) / 3;
	}
}
